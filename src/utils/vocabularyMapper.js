// Get Thai meaning for a romaji word
export const getThaiMeaning = (romajiWord) => {
  // Remove punctuation for lookup
  const cleanWord = romajiWord.replace(/[.,!?]/g, '')
  
  // Try exact match first
  if (vocabularyMap[cleanWord]) {
    return vocabularyMap[cleanWord]
  }
  
  // Try case-insensitive match
  const lowerWord = cleanWord.toLowerCase()
  for (const [key, value] of Object.entries(vocabularyMap)) {
    if (key.toLowerCase() === lowerWord) {
      return value
    }
  }
  
  // Return empty string if not found
  return ""
}

// Get vocabulary with meaning for a token (keeps punctuation)
export const getTokenWithMeaning = (token) => {
  const meaning = getThaiMeaning(token)
  return {
    word: token,
    meaning_th: meaning
  }
}
